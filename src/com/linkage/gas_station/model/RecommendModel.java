public class RecommendModel {
	String recommended_phone="";
	int recommend_state=-1;
	String recommend_time="";
	public String getRecommended_phone() {
		return recommended_phone;
	}
	public void setRecommended_phone(String recommended_phone) {
		this.recommended_phone = recommended_phone;
	}
	public int getRecommend_state() {
		return recommend_state;
	}
	public void setRecommend_state(int recommend_state) {
		this.recommend_state = recommend_state;
	}
	public String getRecommend_time() {
		return recommend_time;
	}
	public void setRecommend_time(String recommend_time) {
		this.recommend_time = recommend_time;
	}
	
	
}
